package aischool.java.Ex02operator.binary_operator;

public class Ex02comparison_oper {
    
    public static void main(String[] args) {
        
        // 비교연산자 (comparison operator)
        // <, >, <=, >=, ==,!=
        // 결과는 boolean(true, false)
        int num1 = 10;
        int num2 = 20;
        
        System.out.println(num1 < num2);
        System.out.println(num1 > num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 == num2);
        System.out.println(num1!= num2);
    }
}
