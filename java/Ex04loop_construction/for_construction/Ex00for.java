package aischool.java.Ex04loop_construction.for_construction;

public class Ex00for {
    
    public static void main(String[] args) {
        
        // 반복문 : 특정 명령을 반복
        // 종류 : for, while, dp-while

        // for : 반복횟수가 정해져 있을 때 사용
        // 문법 구조
        // for(초기값; 조건식; 증감식) {
        //     반복할 명령;
        // }
        
        for(int i = 1; i <= 10; i++) {
            System.out.print(i+" ");
        }
    }
}
