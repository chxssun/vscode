

public class Ex01variable {
    
    public static void main(String[] args) {
        
        // 변수 : 데이터를 저장할 수 있는 공간
        // 1) 변수 선언
        int num;
        
        // 2) 변수 초기화
        num = 123;
        
        // 변수 선언 및 초기화
        int num2 = 123;
        
        // 변수 값 재할당
        num2 = 456;
        
        System.out.println(num);
        System.out.println(num2);
        
    }
}
