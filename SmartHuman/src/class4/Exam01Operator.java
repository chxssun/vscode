public class Exam01Operator {
    public static void main(String[] args) {
        int num1 = 11;
        int num2 = 3;
        System.out.println(num1+num2);
        System.out.println(num1-num2);
        System.out.println(num1*num2);
        System.out.println(num1/num2);
        System.out.println(num1%num2); 

    }
}
